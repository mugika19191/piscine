int	ft_is_prime(int nb)
{
	int	i;

	i = nb;
	if (nb == 0 || nb == 1)
		return (0);
	while (i > 0)
	{
		if (i != nb && i != 1)
		{
			if ((nb % i) == 0)
				return (0);
		}
		i--;
	}
	return (1);
}
